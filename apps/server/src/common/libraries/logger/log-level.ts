export enum LogLevel
  {
	 /** For tracing purposes. */
	 TRACE   = 'TRACE',
	 /** For verbose debugging purposes. */
	 VERBOSE = 'VERBOSE',
	 /** For debugging purposes. */
	 DEBUG   = 'DEBUG',
	 /** For informational messages. */
	 INFO    = 'INFO',
	 /** For warnings. */
	 WARN    = 'WARN',
	 /** For errors. */
	 ERROR   = 'ERROR',
	 /** For fatal errors. */
	 FATAL   = 'FATAL',
  }